// FAÇA UMA FUNÇÃO QUE RECEBA 3 NOTAS E RETORNE A MÉDIA DO ALUNO EM JAVASCRIPT.

function notas (nota1, nota2, nota3){ // CREI UMA FUNÇÃO NOTAS 1,2,3 
     return (nota1 + nota2 + nota3) / 3;  // RETORNANDO A MÉDIA  DAS 3 NOTAS 
}
  console.log(notas(5, 4, 9)); // CHAMEI A FUNÇÃO E COLOQUEI AS NOTAS PARA CALCULAR A MÉDIA

 